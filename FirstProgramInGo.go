// Package Declaration
// Packages are used to organize and reuse the code
package main

// Import Statement
// Import Statements are used to import packages from Golang's Standard Library.
import (
	// fmt stands for 'Format' package
	"fmt"
)

// Main Function
func main() {

	// Println() stands for 'Print Line' which is a function we use to print something.
	// This function lies in the fmt package
	fmt.Println("Hello World!")

}
